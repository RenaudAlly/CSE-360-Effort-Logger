package application;

public class ID {

	private String user;
	private String pass;
	
	public ID() {
		
		user = "";
		pass = "";
		
	}
	
	public String getUser() {
		
		return user;
		
	}
	
	public String getPass() {
		
		return pass;
		
	}
	
	public void setUser(String s) {
		
		this.user = s;
		
	}
	
	public void setPass(String s) {
		
		this.pass = s;
		
	}
	
}
